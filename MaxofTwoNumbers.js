function maxOfTwo(firstNumber, secondNumber){
    
  return firstNumber > secondNumber ? firstNumber : secondNumber;
  
}

console.log('Max of 2 and 4 is ->', maxOfTwo(2, 4));
console.log('Max of 46 and 14 is ->', maxOfTwo(46, 14));
console.log('Max of 46 and 14 is ->', maxOfTwo(46, 14));
console.log('Max of -10 and -100 is ->', maxOfTwo(-10, -100));
